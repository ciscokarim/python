import random

#random_number below is just a name, it could be anything.
#randint is a keyword and it has to be as it is.
random_number=random.randint(1,10)
print(f"random_number this time is : {random_number}")

#random() key word, it will only generate a number between 0 and one.
#you can make the number bigger or smaller by multiplying or adding something
#to it.
random_float=random.random()
print(f"random float number this time is : {random_float}")
